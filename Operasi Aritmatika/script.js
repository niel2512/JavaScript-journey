// % = Modulo, ++ = Increment, -- = Decrement, ** = Pangkat

let x = 10
console.log(x + 1.5);
console.log(x - 1.5);
console.log(x / 1.5);
console.log(x * 1.5);

// Aritmatika Khusus JS
console.log(x % 4);
console.log(x ** 2);

// pemrograman biasa 
x = x + 1;
console.log(x);
x++
console.log(x);

x = x - 1;
console.log(x);
x--;
console.log(x);

