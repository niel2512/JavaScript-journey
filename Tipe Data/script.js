// String
let nama = "Niel";
console.log(nama);
console.log(typeof nama);
// Overloading
nama = 1;
console.log(nama);
console.log(typeof nama);

// Number
let nomor = 3.14;
console.log(nomor);
console.log(typeof nomor);

// Boolean
let bool = true;
console.log(bool);
console.log(typeof bool);

// Undefined
let data;
console.log(data);
console.log(typeof data);
// Overloading
data = "nels";
console.log(data);
console.log(typeof data);
